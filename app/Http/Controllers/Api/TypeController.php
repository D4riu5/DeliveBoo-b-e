    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $types = Type::all();

        return response()->json([
            'success' => true,
            'code' => 200,
            'message' => 'ok',
            'types' => $types
        ]);
    }

     /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($name)
    {
        $type = Type::where('name', $name)->with('restaurants')->first();

        
        if ($type) {
            return response()->json([
                'success' => true,
                'code' => 200,
                'message' => 'ok',
                'type' => $type
            ]);
        }
        else {
            return response()->json([
                'success' => false,
                'code' => 404,
                'message' => 'Not Found',
            ]);
        }
    }


